# sort the list alphabetically

thislist = ["mango", "papaya", "orange", "banana"]
thislist.sort()
print(thislist)

# sort the list numerically
thislist = [89, 90, 48, 30]
thislist.sort()
print(thislist)

# sort the list descending order
thislist = ["mango", "banana", "pineapple", "orange"]
thislist.sort(reverse=True)
print(thislist)

thislist = [100,58,39,32,59]
thislist.sort(reverse=True)
print(thislist)

