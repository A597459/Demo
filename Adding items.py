# Adding an item to the dictionary is done by using a new index key and assigning a value to it:
thisdict = {
    "model": "s88",
    "brand": "mahendra"
}
thisdict["year"] = 2021
print(thisdict)

# Add a color item to the dictionary by using the update() method:
thisdict = {
    "brand": "mahendra",
    "model": "j84"
}
thisdict["color"] = "white"
print(thisdict)

