# Create and print a dictionary:
thisdict = {
    "Brand": "Mahendra",
    "Model": "S10",
    "Year": "2019",
}
print(thisdict)

# print the brand value of dictionary:
thisdict = {
    "Brand": "Mahendra",
    "Model": "S10",
    "Year": "2019"
}
print(thisdict["Brand"])

# duplicate value will overwrite existing value:
thisdict = {
    "Brand": "Mahendra",
    "Model": "S10",
    "Year": "2019",
    "Year": "2018"
}
print(thisdict)

# Print the number of item in the dictionary:
thisdict = {
    "Brand": "Mahendra",
    "Model": "S10",
    "Year": "2020"
}
print(len(thisdict))

# Print the data type of a dictionary:
thisdict = {
    "Brand": "Mahendra",
    "Model": "S10",
    "Year": "2020"
}
print(type(thisdict))



